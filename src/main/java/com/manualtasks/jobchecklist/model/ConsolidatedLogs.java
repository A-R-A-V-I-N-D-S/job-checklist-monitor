package com.manualtasks.jobchecklist.model;

public class ConsolidatedLogs {

	private String jobOrLogName;
	
	private String errors;

	public String getJobOrLogName() {
		return jobOrLogName;
	}

	public void setJobOrLogName(String jobOrLogName) {
		this.jobOrLogName = jobOrLogName;
	}

	public String getErrors() {
		return errors;
	}

	public void setErrors(String errors) {
		this.errors = errors;
	}
	
}
